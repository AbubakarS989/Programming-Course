#include<stdio.h>
int main(){
 
 
    // These operators are used to reduce work and make our program looks clean
    int var;

    var=10;//assign right to left
    
    var+=5; // add 5 to var ->var=var+5
    
    var-=5; // subtract 5 to var ->var=var-5
    
    var*=5; // multiply 5 with var -> var=var*5
    
    var/=5; // divide 5 to var -> var=var/5
    
    var%=5; // modulus 5 to var -> var=var%5
    
               

}